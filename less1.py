'''5. Напишите программу, которая принимает на вход координаты двух точек и находит 
расстояние между ними в 2D пространстве.'''

import math

ax = float(input("Координаты точки a по оси x: "))
ay = float(input("Координаты точки a по оси y: "))
bx = float(input("Координаты точки a по оси x: "))
by = float(input("Координаты точки a по оси y: "))

distance = math.sqrt((ax-bx)**2 + (ay-by)**2)
print(f'Расстояние между точками равняется {distance}')
