'''3. Напишите программу, которая принимает на вход координаты точки (X и Y), причём X ≠ 0 и Y ≠ 0 и выдаёт номер 
четверти плоскости, в которой находится эта точка (или на какой оси она находится).'''

x = int(input("Введите X ≠ 0: "))
y = int(input("Введите Y ≠ 0: "))

if x == 0 and y == 0:
    print("Неверные координаты!")
elif x > 0 and y > 0:
    print("Ваши координаты в первой плоскости!")
elif x < 0 and y > 0:
    print("Ваши координаты во второй плоскости!")
elif x < 0 and y < 0:
    print("Ваши координаты в третьей плоскости!")
elif x > 0 and y < 0:
    print("Ваши координаты в четвертой плоскости!")


